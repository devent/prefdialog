package com.globalscalingsoftware.prefdialog;

/**
 * The options the user choose to close the preference dialog.
 */
public enum Options {
	/**
	 * The user canceled the dialog by either clicking on the "Cancel" button or
	 * by otherwise closing it.
	 */
	CANCEL,

	/**
	 * The user approved the input in the dialog by clicking on the "Ok" button.
	 */
	OK
}
