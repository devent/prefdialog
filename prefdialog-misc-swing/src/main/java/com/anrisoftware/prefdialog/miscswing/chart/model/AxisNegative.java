package com.anrisoftware.prefdialog.miscswing.chart.model;

/**
 * Sets plot axis negative, i.e. the axis starts with zero to {@code -∞}.
 * 
 * @author Erwin Mueller, erwin.mueller@deventm.org
 * @since 3.0
 */
public enum AxisNegative {

	/**
	 * Axis starts with zero to {@code -∞}.
	 */
	NEGATIVE,

	/**
	 * Axis starts with zero to {@code +∞}.
	 */
	POSITIVE
}
